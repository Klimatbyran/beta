export type Region = keyof typeof regions;
export type Municipality = typeof regions[Region][number];

// Get all unique municipalities
export const allMunicipalities = Object.values(regions).flat();

// Get region for a municipality
export function getRegionForMunicipality(municipality: string): Region | undefined {
  return Object.entries(regions).find(([_, municipalities]) => 
    municipalities.includes(municipality)
  )?.[0] as Region | undefined;
}

// Generate a unique ID for a municipality
export function getMunicipalityId(municipality: string): string {
  return municipality
    .toLowerCase()
    .replace(/[åä]/g, 'a')
    .replace(/[ö]/g, 'o')
    .replace(/[é]/g, 'e')
    .replace(/[\s-]+/g, '-')
    .replace(/[^a-z0-9-]/g, '');
}

// Get municipality by ID
export function getMunicipalityById(id: string): Municipality | undefined {
  return allMunicipalities.find(municipality => 
    getMunicipalityId(municipality) === id
  );
}